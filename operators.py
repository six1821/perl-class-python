# arithmetic operators
a = 10
b = 5
print(f"Addition operator {a + b}")
print(f"subtraction operator {a-b}")
print(f"multiplication operator {a*b}")
print(f"division operator {a/b}")
print(f"exponent operator {a**b}")
print(f"floor division operator {a//b}")

# comparison operators
c = 20
d = 10
print(f"{c} is equal to {d}: {c==d}")
print(f"{c} is greater than {d}: {c>d}")

#logical operators
e = 16
print(f"is this statement true or false:",e>5 and e<16)
print(f"is this statement true or false:",e>5 or e<16)

